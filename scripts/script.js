function getRandomKey(obj) {
    var result;
    var count = 0;
    for (var prop in obj)
        if (Math.random() < 1/++count)
           result = prop;
    return result;
}

function maleOrFemale() {
    return Math.round(Math.random());
}
 
function pickProfiles() {
    var propList = [];
    while (propList.length < 16) {
        
        gender = maleOrFemale();

        if (gender == 0) {
            daKey = getRandomKey(Females);
            if (propList.includes(daKey) == true) {
                continue
            }
            propList.push(daKey);
    
            var content = "<div class='card'><div><img src='" + Females[daKey].image + "' alt=''><h2 class='username'>" + Females[daKey].name + "</h2><h3 class='age'>" + Females[daKey].age + " years old</h3><h3 class='species'>" + Females[daKey].species + "</h3></div></div>"
    
            main[0].innerHTML = content + main[0].innerHTML;
        }
        else if (gender == 1) {
            daKey = getRandomKey(Males);
            if (propList.includes(daKey) == true) {
                continue
            }
            propList.push(daKey);
    
            var content = "<div class='card'><div><img src='" + Males[daKey].image + "' alt=''><h2 class='username'>" + Males[daKey].name + "</h2><h3 class='age'>" + Males[daKey].age + " years old</h3><h3 class='species'>" + Males[daKey].species + "</h3></div></div>"
    
            main[0].innerHTML = content + main[0].innerHTML;
        }
    }

}

